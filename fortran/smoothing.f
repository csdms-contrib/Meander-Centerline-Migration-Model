c-----------------------------------------------------------------------
c CURVATURE SMOOTHING
c-----------------------------------------------------------------------
      subroutine smoothing(N, c)
      implicit none
      integer j, N
      real*8 c(N), aux(N)

      aux(:) = c(:)

      do j = 2, N-1
	    aux(j) = ( c(j-1) + 2.d0*c(j) + c(j+1) ) / 4.d0
      end do

      c(:) = aux(:)

c-----------------------------------------------------------------------
c end of subroutine
c-----------------------------------------------------------------------
      return
      end